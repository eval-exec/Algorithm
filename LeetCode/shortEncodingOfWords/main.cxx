class Solution {
  public:
    int minimumLengthEncoding(vector<string> &ws) {
        unordered_map<char, Node *> roots;
        int ret = 0;
        unordered_set<string> words;
        for (const auto &word:ws) {
            words.insert(word);
        }
        for (const auto &word : words) {
            int len = word.size();
            char last = word[len - 1];
            auto f = roots.find(last);
            if (f == roots.end()) {
                roots[last] = new Node(last);
                ret += len + 1;
                put(roots[last], word.substr(0, len - 1));
            } else {
                Node *root = f->second;
                int nd = 0;
                bool fork = false;
                for (int i = len - 2; i >= 0; i--) {
                    if (root->nexts.find(word[i]) == root->nexts.end()) {
                        nd++;
                        if (!root->nexts.empty()) fork = true;
                    }
                    root = get(root, word[i]);
                }
                if (fork) {
                    ret += len + 1;
                } else {
                    ret += nd;
                }
            }
        }
        return ret;
    }
  private:
    class Node {
      public:
        char c;
        map<char, Node *> nexts;

        Node(char v) {
            c = v;
        };
    };
    void put(Node *node, string str) {
        if (str.empty()) return;
        for (int i = str.size() - 1; i >= 0; i--) {
            node = get(node, str[i]);
        }
    }
    Node *get(Node *node, char c) {
        auto f = node->nexts.find(c);
        if (f == node->nexts.end()) {
            Node *nc = new Node(c);
            node->nexts[c] = nc;
            return nc;
        }
        return f->second;
    }
};

